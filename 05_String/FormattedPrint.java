public class FormattedPrint {
    public static void main(String[] args) {

        float f = 356.15748f;
        System.out.printf("formatted value %.2f",f);
        System.out.println();
        System.out.printf("PI %.6f",Math.PI);
        System.out.println();
        System.out.printf("hello %d name is %s",5,"raj");
    }
}
